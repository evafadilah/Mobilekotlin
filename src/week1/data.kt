package week1

fun main() {
    //Tipe Data String
    val nama= "Eva" //String

    //Tipe Data Int(Integer)
    val usia= 18 //Integer/Int

    //Tipe Data Double
    val tinggi= 169.0 //Double

    println("nama saya $nama usia saya $usia tahun dan tinggi badan saya $tinggi cm")

    //Tipe Data Char
    val char = 'A'
    println(char)

    //Tipe Data Boolean
    val benar = true
    val salah = false
    println("jika $benar maka tidak $salah")

}